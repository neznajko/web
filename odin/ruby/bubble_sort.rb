def suap( a, i, j )
  t = a[i]
  a[i] = a[j]
  a[j] = t
end
################################################################
def bubble_sort( a )
  n = a.length
  while n > 0
    p a
    done = true
    (1...n).each do |j|
      if a[j] < a[j - 1]
        suap( a, j - 1, j )
        done = false
      end
    end
    break if done
    n -= 1
  end
end
